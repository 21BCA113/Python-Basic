#Escape sequences in string

# \n    in a string denotes a new line
# \t    in a string denotes a tab
# \     in a string denotes a single quote '
# \\    in a string denotes a backslash \    it is also the reason we use double backspaces while entering a path.
# Examples
ak = "Hello \n My Python High\\school \t player"
print(ak)